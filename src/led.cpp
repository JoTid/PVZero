#define LED1_PIN  12


/*--------------------------------------------------------------------------------------------------------------------*\
** Method implementation                                                                                              **
**                                                                                                                    **
\*--------------------------------------------------------------------------------------------------------------------*/  

//--------------------------------------------------------------------------------------------------------------------//
//                                                                                                                    //
//                                                                                                                    //
//--------------------------------------------------------------------------------------------------------------------// 
void ledInit() 
{
  Serial.printf("Initialise LED...\n");

  //--------------------------------------------------------------------------------------------------- 
  // Configure the port pin direction and set the output to low
  // 
  pinMode(LED1_PIN, OUTPUT);
  digitalWrite(LED1_PIN, LOW);
}

//--------------------------------------------------------------------------------------------------------------------//
//                                                                                                                    //
//                                                                                                                    //
//--------------------------------------------------------------------------------------------------------------------// 
void ledSet(int slLedV, bool btValueV)
{
  //--------------------------------------------------------------------------------------------------- 
  // handle one LED
  //
  if (btValueV)
  {
    digitalWrite(LED1_PIN, HIGH);
  }
  else
  {
    digitalWrite(LED1_PIN, LOW);
  }
}

//--------------------------------------------------------------------------------------------------------------------//
//                                                                                                                    //
//                                                                                                                    //
//--------------------------------------------------------------------------------------------------------------------// 
bool ledIsOn(int slLedV)
{
  bool btReturnT = false;

  //--------------------------------------------------------------------------------------------------- 
  // handle one LED
  //
  if (digitalRead(LED1_PIN) > 0)
  {
    btReturnT = true;
  }

  return btReturnT;
}

